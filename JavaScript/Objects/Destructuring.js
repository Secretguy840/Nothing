// Define an object
const person = {
    firstName: "John",
    lastName: "Doe",
    age: 30,
    city: "New York"
};

// Destructuring assignment
const { firstName, lastName, age, city } = person;

// Display the destructured values
console.log(`First Name: ${firstName}`);
console.log(`Last Name: ${lastName}`);
console.log(`Age: ${age}`);
console.log(`City: ${city}`);
